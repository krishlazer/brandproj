package krish.practice.handson20;

public class BrandClutter 
{
	private String BrandName;
	private String BrandOrigin;
	private int FoundedYear;
	private String OwningCompany;
	
	public String getBrandName() 
	{
		return BrandName;
	}
	public void setBrandName(String BrandName) 
	{
		this.BrandName = BrandName;
	}
	public String getBrandOrigin() 
	{
		return BrandOrigin;
	}
	public void setBrandOrigin(String BrandOrigin) 
	{
		this.BrandOrigin = BrandOrigin;
	}
	public int getFoundedYear() 
	{
		return FoundedYear;
	}
	public void setFoundedYear(int FoundedYear) 
	{
		this.FoundedYear = FoundedYear;
	}
	public String getOwningCompany() 
	{
		return OwningCompany;
	}
	public void setOwningCompany(String OwningCompany) 
	{
		this.OwningCompany = OwningCompany;
	}
}
